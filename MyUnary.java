class MyUnary {
  public static void main(String[] args) {
    int a = 1;
    int finl = +a;
    System.out.println(finl);
    int finla = -a;
    System.out.println(finla);
    int finlb = ++a;
    System.out.println(finlb);
    int finlc = --a;
    System.out.println(finlc);
    boolean finlbc = true;
    boolean finlbbbbb = !finlbc;
    System.out.println(finlbbbbb);
     
  }
}